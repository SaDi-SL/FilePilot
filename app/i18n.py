# ── Runtime state ─────────────────────────────────────────────────────────────
_current_lang: str = "en"


def set_language(lang: str) -> None:
    """Switch active language."""
    global _current_lang
    if lang not in LANGUAGES:
        raise ValueError(f"Language '{lang}' not available. Choose: {list(LANGUAGES.keys())}")
    _current_lang = lang


def get_language() -> str:
    """Return current language code."""
    return _current_lang


def t(key: str, *args) -> str:
    """
    Translate key. Falls back to English if missing.
    Supports format args: t('msg_theme_switched', 'dark')
    """
    lang_dict = LANGUAGES.get(_current_lang, LANGUAGES["en"])
    text = lang_dict.get(key) or LANGUAGES["en"].get(key) or key
    if args:
        try:
            text = text.format(*args)
        except Exception:
            pass
    return text


def available_languages() -> list:
    """Return list of (code, display_name) tuples."""
    return [(code, data["lang_name"]) for code, data in LANGUAGES.items()]


def language_display_name(code: str) -> str:
    """Return display name for a language code."""
    return LANGUAGES.get(code, {}).get("lang_name", code)